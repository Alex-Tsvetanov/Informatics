#include <iostream>
using namespace std;
int main()
{
long long a,b,c,br;
cin>>a>>b>>c;
for(br=0;br==br;br=br)
{
    a--;
    br++;
    if(a==0||b==0||c==0)break;
    b--;
    br++;
    if(a==0||b==0||c==0)break;
    c--;
    br++;
    if(a==0||b==0||c==0)break;
    b--;
    br++;
    if(a==0||b==0||c==0)break;
}
cout<<br<<endl;
return 0;
}
